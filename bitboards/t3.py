def bin64_to_str(bin64):
    return str(bin(bin64))[2:].zfill(64)

def str_to_bin64(s):
    total = 0
    for idx, val in enumerate(s):
        c = 63 - idx
        total += 2 ** c if val == "1" else 0
    return total

